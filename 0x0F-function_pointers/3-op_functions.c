#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub -  subtract one integer from another integer
 * @a: first integer
 * @b: second integer
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide an integer by another
 * @a: first integer
 * @b: second integer
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_add -modulus of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
